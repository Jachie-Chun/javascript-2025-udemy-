// Operaciones en JavaScript

const n1 = 30;
const n2 = 20;

let resultado;

// Suma

resultado = n1 + n2;

// Resta 

resultado = n1 - n2;

// Multipicación 

resultado = n1 * n2;   // 600

// División 

resultado = n1 / n2;   // 1.5

// Modulo

resultado = n1 % n2;   // 10


console.log(resultado);